    private void saveAsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAsActionPerformed
            
        saveAs = (JComboBox) evt.getSource();
            
        
        
            n.text  = content.getText();
            n.setTitle(title.getText());
            n.setAuthor(author.getText());
        
            if("SaveAsTxt".equals(saveAs.getSelectedItem())){
                n.speichernAls(new AlsTextSpeichern());
            }
            else if("SaveAsDocx".equals(saveAs.getSelectedItem())){
                n.speichernAls(new AlsDocxSpeichern());
            }
            else if("SafeAsMarkdown".equals(saveAs.getSelectedItem())){
                n.speichernAls(new AlsMarkdownSpeichern());
            }
    }//GEN-LAST:event_saveAsActionPerformed

